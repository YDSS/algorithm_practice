/**
 * @param {character[][]} board
 * @return {boolean}
 */
function isValidSudoku(board) {
    let checkSubBox = (i, j) => {
        let isValid = true;
        let map = [];
        for (let x = i; x < i + 3; x++) {
            for (let y = j; y < j + 3; y++) {
                let ceil = board[x][y];
                if (ceil === ".") {
                    continue;
                }
                if (map[ceil] != null) {
                    isValid = false;
                    break;
                } else {
                    map[ceil] = 1;
                }
            }
        }

        return isValid;
    };
    for (let i = 0; i < 7; i += 3) {
        for (let j = 0; j < 7; j += 3) {
            if (!checkSubBox(i, j)) {
                // console.log(`i: ${i}, j: ${j}`);
                return false;
            }
        }
    }

    return true;
}

